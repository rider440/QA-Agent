class TestGenerator:
    """
    Generates pytest test files from a `TestPlan` using an LLM.
    """
 
    def __init__(self, llm: BaseChatModel, settings: dict):
        self.llm = llm
        self.settings = settings
        self.base_url: str = settings["test_generation"]["base_url"]
        self.browser: str = settings["playwright"]["browser"]
        self.headless: bool = settings["playwright"]["headless"]
        self.out_dir = Path(settings["paths"]["tests_generated"])
 
        self._api_chain = (
            ChatPromptTemplate.from_messages(
                [("system", API_TEST_SYSTEM), ("human", "{spec}")]
            )
            | self.llm
            | StrOutputParser()
        )
        self._ui_chain = (
            ChatPromptTemplate.from_messages(
                [("system", UI_TEST_SYSTEM), ("human", "{spec}")]
            )
            | self.llm
            | StrOutputParser()
        )
 
    # ── Private ──────────────────────────────────────────────────────────────
 
    @staticmethod
    def _sanitise_name(text: str) -> str:
        """Convert arbitrary text to a safe Python identifier fragment."""
        return re.sub(r"[^a-z0-9]+", "_", text.lower()).strip("_")[:40]
 
    async def _write(self, path: Path, content: str) -> None:
        path.parent.mkdir(parents=True, exist_ok=True)
        async with aiofiles.open(path, "w", encoding="utf-8") as fh:
            await fh.write(content)
        logger.info("Generated: %s", path)
 
    async def _generate_api_test(self, tc: APITestCase) -> tuple[str, str]:
        """Return (filename, file_content) for one API test case."""
        spec = (
            f"Endpoint: {tc.method} {tc.endpoint}\n"
            f"Description: {tc.description}\n"
            f"Expected HTTP status: {tc.expected_status}\n"
            f"Payload: {tc.payload}"
        )
        func_code: str = await self._api_chain.ainvoke({"spec": spec})
        name = self._sanitise_name(f"{tc.method}_{tc.endpoint}")
        filename = f"test_{name}.py"
        header = FILE_HEADER.format(filename=filename, base_url=self.base_url)
        return filename, f"{header}\n\n{func_code.strip()}\n"
 
    async def _generate_ui_test(self, tc: UITestCase) -> tuple[str, str]:
        """Return (filename, file_content) for one UI test case."""
        spec = (
            f"Page URL: {self.base_url}{tc.page}\n"
            f"Action: {tc.action}\n"
            f"Assertion: {tc.assertion}"
        )
        func_code: str = await self._ui_chain.ainvoke({"spec": spec})
        name = self._sanitise_name(tc.page)
        filename = f"test_{name}.py"
        header = UI_FILE_HEADER.format(filename=filename, base_url=self.base_url)
        return filename, f"{header}\n\n{func_code.strip()}\n"
 
    # ── Public ───────────────────────────────────────────────────────────────
 
    async def generate(self, plan: TestPlan, _codebase_summary: str) -> list[str]:
        """
        Generate all test files from the plan.
        Returns a list of file paths that were written.
        """
        written: list[str] = []
 
        # conftest.py
        conftest_content = CONFTEST_TEMPLATE.format(
            base_url=self.base_url,
            browser=self.browser,
            headless=str(self.headless),
        )
        conftest_path = self.out_dir / "conftest.py"
        await self._write(conftest_path, conftest_content)
        written.append(str(conftest_path))
 
        # Semaphore and retry logic to avoid triggering 429 RESOURCE_EXHAUSTED
        max_concurrency = self.settings.get("test_generation", {}).get("max_concurrency", 1)
        delay = self.settings.get("test_generation", {}).get("rate_limit_delay_seconds", 15)
        sem = asyncio.Semaphore(max_concurrency)

        async def _run_with_retry(func, tc, max_retries=5):
            backoff = 60
            for attempt in range(max_retries):
                try:
                    async with sem:
                        result = await func(tc)
                        if delay > 0:
                            await asyncio.sleep(delay)
                        return result
                except Exception as e:
                    err_str = str(e).upper()
                    if any(x in err_str for x in ["429", "RESOURCE_EXHAUSTED", "503"]):
                        logger.warning(f"Quota hit or service busy. Waiting {backoff}s before retry (Attempt {attempt+1}/{max_retries})...")
                        await asyncio.sleep(backoff)
                        backoff *= 2
                    else:
                        raise e
            raise Exception(f"Failed to generate test after {max_retries} attempts.")

        # API tests
        if self.settings["test_generation"]["generate_api_tests"]:
            api_tasks = [_run_with_retry(self._generate_api_test, tc) for tc in plan.api_tests]
            api_results = await asyncio.gather(*api_tasks)
            for filename, content in api_results:
                path = self.out_dir / "api" / filename
                await self._write(path, content)
                written.append(str(path))

        # UI tests
        if self.settings["test_generation"]["generate_ui_tests"]:
            ui_tasks = [_run_with_retry(self._generate_ui_test, tc) for tc in plan.ui_tests]
            ui_results = await asyncio.gather(*ui_tasks)
            for filename, content in ui_results:
                path = self.out_dir / "ui" / filename
                await self._write(path, content)
                written.append(str(path))

        logger.info("Generator: wrote %d files.", len(written))
        return written
 
